package Topics.Basics;

public class Maths {
    public static void main(String[] args) {

        System.out.println(Math.max(5, 10)); // Prints the highest value

        System.out.println(Math.min(5, 10)); // Prints the lowest value

        System.out.println(Math.sqrt(64)); // Square root of the value

        System.out.println(Math.abs(-4.7)); // Prints absolute (positive) value of x

        System.out.println(Math.random()); // Prints random number between 0 and 1


    }
}
