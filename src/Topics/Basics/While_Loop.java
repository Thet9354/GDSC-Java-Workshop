package Topics.Basics;

public class While_Loop {
    public static void main(String[] args) {

        // While Loop
        int i = 0;
        while (i < 5) {
            System.out.println(i);
            i++;
        }

        // Do/While Loop
        int x = 0;
        do {
            System.out.println(i);
            x++;
        }
        while (x < 5);
    }
}
